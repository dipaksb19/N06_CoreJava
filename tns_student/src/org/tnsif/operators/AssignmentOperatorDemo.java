package org.tnsif.operators;

public class AssignmentOperatorDemo {

	public static void main(String[] args) {
		System.out.println("****Assignment Operators****");
		int num1 = 20;
		int num2 = 10;
		num1 -= num2; // x=x-y
		System.out.println(num1);
		
		num1 += num2;
		System.out.println(num1);

	}

}
